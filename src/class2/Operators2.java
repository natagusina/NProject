package class2;

public class Operators2 {
    public static void main(String[] args) {

        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        // put your cursor anywhere o a line and press Ctrl+D or CMD+D to duplicate it
        System.out.println(number1-number2);
        System.out.println(number1/number2);
        System.out.println(number1*number2);

        /*
        if you have to store whole numbers always use int
        if you have to store decimal number always double
        if you have to store character you can always use String
         */


    }
}
